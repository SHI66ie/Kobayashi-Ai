// Helper function to get historical results for a race
export function getHistoricalResults(raceName: string, raceDate: string): { position: number; driver: string; team: string }[] | null {
  const year = new Date(raceDate).getFullYear();
  const normalizedRaceName = raceName.toLowerCase()
    .replace(/ grand prix/gi, '')
    .replace(/ gp$/i, '')
    .replace(/[^a-z]/g, '')
    .trim();
  
  // Try to find matching race in historical data
  const yearData = historicalF1Results[year as keyof typeof historicalF1Results];
  if (!yearData) return null;
  
  // Find matching race by checking various name patterns
  for (const [key, race] of Object.entries(yearData)) {
    const normalizedKey = key.replace(/[^a-z]/g, '').trim();
    
    if (normalizedKey === normalizedRaceName || 
        normalizedRaceName.includes(normalizedKey) ||
        normalizedKey.includes(normalizedRaceName)) {
      return race.podium;
    }
  }
  
  return null;
}

// Helper function to get all available years
export function getAvailableYears(): number[] {
  return Object.keys(historicalF1Results).map(Number).sort((a, b) => b - a);
}

// Helper function to get all races for a year
export function getRacesForYear(year: number): Array<{key: string; name: string; date: string; podium: any[]}> {
  const yearData = historicalF1Results[year as keyof typeof historicalF1Results];
  if (!yearData) return [];
  
  return Object.entries(yearData).map(([key, race]) => ({
    key,
    name: race.name,
    date: race.date,
    podium: race.podium
  }));
}
